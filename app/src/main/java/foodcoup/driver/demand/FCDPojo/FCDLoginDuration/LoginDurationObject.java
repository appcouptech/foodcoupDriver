package foodcoup.driver.demand.FCDPojo.FCDLoginDuration;

public class LoginDurationObject {

    private String d_image ;
    private String d_name ;
    private String date ;
    private String duty_count ;
    private String total_duration ;


    public String getD_image() {
        return d_image;
    }

    public void setD_image(String d_image) {
        this.d_image = d_image;
    }

    public String getD_name() {
        return d_name;
    }

    public void setD_name(String d_name) {
        this.d_name = d_name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDuty_count() {
        return duty_count;
    }

    public void setDuty_count(String duty_count) {
        this.duty_count = duty_count;
    }

    public String getTotal_duration() {
        return total_duration;
    }

    public void setTotal_duration(String total_duration) {
        this.total_duration = total_duration;
    }
}
